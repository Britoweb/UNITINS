package entidade;

public class Matematica {
	
	public float subtrair(int x, float y) {
		return x-y;
	}
	
	public double subtrair(float x, double y) {
		return x-y;
	}
	
	public double subtrair(int x, double y) {
		return x-y;
	}
	
	
	
	public float somar(int x, float y) {
		return x+y;
	}
	
	public double somar(float x, double y) {
		return x+y;
	}
	
	public double somar(int x, double y) {
		return x+y;
	}
	
	
	
	
	public float dividir(int x, float y) {
		return x/y;
	}
	
	public double dividir(float x, double y) {
		return x/y;
	}
	
	public double dividir(int x, double y) {
		return x/y;
	}
	
	
	
	
	public float multiplicar(int x, float y) {
		return x*y;
	}
	
	public double multiplicar(float x, double y) {
		return x*y;
	}
	
	public double multiplicar(int x, double y) {
		return x*y;
	}
}

